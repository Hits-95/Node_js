//area of triangle...
exports.areaTriangle = function (base, hieght) {
    return (1 / 2) * base * hieght;
}

//area of circle...
exports.areaCircle = (radius) => {
    return Math.PI * radius * radius;
}

//Circumference of circle...
exports.areaCircumference = (radius) => {
    return Math.PI * (radius * 2);
}